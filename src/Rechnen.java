public class Rechnen
{

    public static void main(String[] args)
    {
        System.out.println(add(2, 3));
        System.out.println(add(1.5, 2.3));
    }

    public static int add(int a, int b)
    {
        return a + b;
    }

    public static double add(double a, double b)
    {
        return a + b;
    }

}
